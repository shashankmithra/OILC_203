package flowcontrols;
/**
 * using continue statement WAP
 * @author sg22080
 *
 */
public class ContinueDemo {

	public static void main(String[] args) {
		for(int i=1;i<=10;i++) {
			if(i==6)
				continue;
		    System.out.println(i);
		}
		System.out.println("----------------------");
		int k;
		for( k=1;k<=6;k++); 
			System.out.println(k);
		
          
	}

}
